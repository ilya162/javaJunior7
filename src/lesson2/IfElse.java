package lesson2;

public class IfElse {
    public static void main(String[] args) {
        int a = 0;
        if (a > 0) {
            System.out.println("+");
        } else if (a < 0) {

            System.out.println("-");
        } else if (a == 0) {

            System.out.println(0);

        } else {
            System.out.println("НЕ число");

        }
    }
}



